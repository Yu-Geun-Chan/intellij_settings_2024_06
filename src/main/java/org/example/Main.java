class Main {
    public static void main(String[] args) {

        // 구현시작
        자동차 a자동차1 = new 자동차();
        a자동차1.번호 = 1;
        a자동차1.최고속력 = 230;

        자동차 a자동차2 = new 자동차();
        a자동차2.번호 = 2;
        a자동차2.최고속력 = 210;



        // 구현끝

        a자동차1.달리다();
        // 출력 => 자동차가 최고속력 230km로 달립니다.

        a자동차2.달리다();
        // 출력 => 자동차가 최고속력 210km로 달립니다.
    }
}

class 자동차 {
    // 구현
    int 번호;
    int 최고속력;
    void 달리다() {
        System.out.printf("%d번 자동차가 최고속력 %dkm로 달립니다.\n", 번호, 최고속력);
    }
}


